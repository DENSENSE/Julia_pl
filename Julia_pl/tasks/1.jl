using HorizonSideRobots

"""
    result!(robot)::Nothing

    Функция создает крестообразный узор из маркеров на сетке, используя робота.

    Алгоритм:
    1. Ставит маркер в начальной позиции
    2. Двигается вверх до границы, ставя маркеры
    3. Возвращается вниз, ставя маркеры
    4. Восстанавливает исходную вертикальную позицию
    5. Повторяет то же самое по горизонтали (влево и вправо)
    6. Возвращается в исходную позицию

    Параметры:
    - robot: Экземпляр робота из библиотеки HorizonSideRobot

    Результат:
    Создает крестообразный узор из маркеров, проходящий через всю сетку
"""
function result!(robot)::Nothing
    a = 0  # Счетчик шагов для возврата в исходную позицию
    putmarker!(robot)  # Ставит маркер в начальной позиции
    while !isborder(robot, Nord)  # Двигается вверх до границы
        move!(robot, Nord)
        putmarker!(robot)  # Ставит маркеры
        a -= 1  # Уменьшает счетчик шагов
    end
    while !isborder(robot, Sud)  # Возвращается вниз
        move!(robot, Sud)
        putmarker!(robot)  # Ставит маркеры
        a += 1  # Увеличивает счетчик шагов
    end
    while a > 0  # Восстанавливает исходную вертикальную позицию
        move!(robot, Nord)
        a -= 1  # Уменьшает счетчик шагов
    end
    while !isborder(robot, West)  # Повторяет то же самое по горизонтали (влево)
        move!(robot, West)
        putmarker!(robot)  # Ставит маркеры
        a -= 1  # Уменьшает счетчик шагов
    end
    while !isborder(robot, Ost)  # Повторяет то же самое по горизонтали (вправо)
        move!(robot, Ost)
        putmarker!(robot)  # Ставит маркеры
        a += 1  # Увеличивает счетчик шагов
    end
    while a > 0  # Возвращается в исходную позицию
        move!(robot, West)
        a -= 1  # Уменьшает счетчик шагов
    end
end
