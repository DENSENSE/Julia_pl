using HorizonSideRobots

"""
    along_rec!(robot, side)

    Рекурсивная функция для движения до упора в заданном направлении.

    Параметры:
    - robot: Экземпляр робота
    - side: Направление движения

    Алгоритм:
    1. Проверяет возможность движения в заданном направлении
    2. Если движение возможно:
       - Делает шаг
       - Рекурсивно вызывает себя для следующего шага
    3. Если движение невозможно - завершает работу

    Особенности:
    - Использует рекурсию вместо цикла
    - Базовый случай: наличие стены в направлении движения
"""
function along_rec!(robot, side)
    if !isborder(robot, side)
        move!(robot, side)
        along_rec!(robot, side)
    end
end
