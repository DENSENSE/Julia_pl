using HorizonSideRobots

"""
    sum_vec(v::AbstractVector{T}, s::T = 0) where T

    Рекурсивная функция для вычисления суммы элементов вектора.

    Параметры:
    - v: Вектор элементов типа T
    - s: Начальная сумма (по умолчанию 0)

    Возвращает:
    Сумму всех элементов вектора v

    Алгоритм:
    1. Если вектор пустой:
       - Возврат накопленной суммы s
    2. Иначе:
       - Добавление последнего элемента вектора к сумме s
       - Рекурсивный вызов для оставшихся элементов

    Особенности:
    - Использует рекурсию для обхода всех элементов вектора
    - Использует макрос @view для создания представления без копирования
    - Обобщенная функция для любого типа T, поддерживающего сложение
"""
function sum_vec(v::AbstractVector{T}, s::T = 0) where T
    if length(v) == 0 
        return s
    end 
    return sum_vec(@view(v[1:end-1]), s + v[end])
end
